function [dsidt, B1term, B2term, B3term, B4term, B5term] = get_dsidt(B1i, B2i, B3i, B4i, B5i, w1, w2, w3, w4, w5, w6, s1, s2)
  
  B1term = B1i{1,1}.*(w1.*w1) + ...
  B1i{1,2}.*(w1.*w2) + ...
  B1i{1,3}.*(w1.*w3) + ...
  B1i{1,4}.*(w1.*w4) + ...
  B1i{1,5}.*(w1.*w5) + ...
  B1i{1,6}.*(w1.*w6) + ...
  B1i{2,1}.*(w2.*w1) + ...
  B1i{2,2}.*(w2.*w2) + ...
  B1i{2,3}.*(w2.*w3) + ...
  B1i{2,4}.*(w2.*w4) + ...
  B1i{2,5}.*(w2.*w5) + ...
  B1i{2,6}.*(w2.*w6) + ...
  B1i{3,1}.*(w3.*w1) + ...
  B1i{3,2}.*(w3.*w2) + ...
  B1i{3,3}.*(w3.*w3) + ...
  B1i{3,4}.*(w3.*w4) + ...
  B1i{3,5}.*(w3.*w5) + ...
  B1i{3,6}.*(w3.*w6) + ...
  B1i{4,1}.*(w4.*w1) + ...
  B1i{4,2}.*(w4.*w2) + ...
  B1i{4,3}.*(w4.*w3) + ...
  B1i{4,4}.*(w4.*w4) + ...
  B1i{4,5}.*(w4.*w5) + ...
  B1i{4,6}.*(w4.*w6) + ...
  B1i{5,1}.*(w5.*w1) + ...
  B1i{5,2}.*(w5.*w2) + ...
  B1i{5,3}.*(w5.*w3) + ...
  B1i{5,4}.*(w5.*w4) + ...
  B1i{5,5}.*(w5.*w5) + ...
  B1i{5,6}.*(w5.*w6) + ...
  B1i{6,1}.*(w6.*w1) + ...
  B1i{6,2}.*(w6.*w2) + ...
  B1i{6,3}.*(w6.*w3) + ...
  B1i{6,4}.*(w6.*w4) + ...
  B1i{6,5}.*(w6.*w5) + ...
  B1i{6,6}.*(w6.*w6);
  
  B2term = B2i{1,1}.*(w1.*s1) + ...
  B2i{1,2}.*(w1.*s2) + ...
  B2i{2,1}.*(w2.*s1) + ...
  B2i{2,2}.*(w2.*s2) + ...
  B2i{3,1}.*(w3.*s1) + ...
  B2i{3,2}.*(w3.*s2) + ...
  B2i{4,1}.*(w4.*s1) + ...
  B2i{4,2}.*(w4.*s2) + ...    
  B2i{5,1}.*(w5.*s1) + ...
  B2i{5,2}.*(w5.*s2) + ...
  B2i{6,1}.*(w6.*s1) + ...
  B2i{6,2}.*(w6.*s2);
  
  B3term = B3i{1,1}.*(s1.*w1) + ...
  B3i{1,2}.*(s1.*w2) + ...
  B3i{1,3}.*(s1.*w3) + ...
  B3i{1,4}.*(s1.*w4) + ...
  B3i{1,5}.*(s1.*w5) + ...
  B3i{1,6}.*(s1.*w6) + ...
  B3i{2,1}.*(s2.*w1) + ...
  B3i{2,2}.*(s2.*w2) + ...
  B3i{2,3}.*(s2.*w3) + ...
  B3i{2,4}.*(s2.*w4) + ...
  B3i{2,5}.*(s2.*w5) + ...
  B3i{2,6}.*(s2.*w6);
  
  B4term = B4i{1,1}.*(s1.*s1) + ...
  B4i{1,2}.*(s1.*s2) + ...
  B4i{2,1}.*(s1.*s2) + ...
  B4i{2,2}.*(s2.*s2);
  
  B5term = B5i;
  
  dsidt = -B1term-B2term-B3term-B4term-B5term;
end
